name = "Viknesh"

# LENGTH 
length = len(name)
print("Length of name : ",length)

# CONCATENATION
name1 = " Abinaya"
joined_name = name + name1
print("Joined name : ",joined_name)

# UPPER CASE
upper_case = name.upper()
print("Upper case name : ",upper_case)

# LOWER CASE
lower_case = name.lower()
print("Lower case name : ",lower_case)

# CAPITALIZE
capitalize_name = name.capitalize()
print("Capitalize name : ",capitalize_name)

# CHECK UPPER CASE
name = name.lower() # viknesh
is_upper = name.isupper()
print("Is upper case ? ",is_upper)

# CHECK LOWER CASE
print("Name : ",name)
is_lower = name.islower()
print("Is lower case ? ",is_lower)